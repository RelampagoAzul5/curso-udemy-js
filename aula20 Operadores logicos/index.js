/*
Operadores lógicos
&& -> AND -> E -> Todas as expressões precisam ser verdadeiras para retornar true
|| -> OR -> OU -> Se qualquer expressão for verdadeira, a expressão toda retorna true 
! -> NOT -> NÃO -> Ele nega a expressao. ou seja, se a expressao for verdadeira e tiver um operador de negação, ela retornará false
*/